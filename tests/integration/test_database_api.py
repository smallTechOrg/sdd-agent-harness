def test_tools_delete_hard_deletes_and_prunes_prompts(client):
    _create(client, "DelTool")
    database_id, v_before, _ = _only_server()
    _rpc(client, database_id, "tools/add", {"definition": _GOOD_TOOL})
    assert any(p["name"] == "explore_recent_orders"
               for p in _rpc(client, database_id, "prompts/list")["result"]["prompts"])
    res = _rpc(client, database_id, "tools/delete", {"name": "recent_orders"})["result"]
    assert res["ok"] is True and res["applied"]["op"] == "delete"
    tools = {t["name"] for t in _rpc(client, database_id, "tools/list")["result"]["tools"]}
    assert "recent_orders" not in tools                                # gone (hard delete)
    prompts = {p["name"] for p in _rpc(client, database_id, "prompts/list")["result"]["prompts"]}
    assert "explore_recent_orders" not in prompts                      # pruned by the cascade


def test_resources_delete_drops_table(client):
    _create(client, "DelRes")
    database_id, _, _ = _only_server()
    resources = _rpc(client, database_id, "resources/list")["result"]["resources"]
    entity = next(r for r in resources if not r["uri"].endswith("/schema"))
    res = _rpc(client, database_id, "resources/delete", {"uri": entity["uri"]})["result"]
    assert res["ok"] is True
    with session_module.create_db_session() as db:
        assert _table_count(db, database_id) == 0                      # backing table dropped + re-inspected


def test_resources_delete_schema_is_rejected(client):
    _create(client, "KeepSchema")
    database_id, _, _ = _only_server()
    resources = _rpc(client, database_id, "resources/list")["result"]["resources"]
    schema = next(r for r in resources if r["uri"].endswith("/schema"))
    resp = _rpc(client, database_id, "resources/delete", {"uri": schema["uri"]})
    assert resp["error"]["code"] == -32602                            # schema resource is undeletable


def test_delete_removes_dir_and_rows(client):
    from data_analysis_agent.config.settings import get_settings
    from data_analysis_agent.tools.table_naming import sql_table_name
    _create(client, "ToDelete")
    database_id, _, _ = _only_server()
    dataset_dir = Path(get_settings().datasets_dir) / sql_table_name("ToDelete")
    assert dataset_dir.exists()

    r = client.post(f"/database/{database_id}/delete", follow_redirects=True)
    assert r.status_code == 200
    with session_module.create_db_session() as db:
        assert db.query(DatabaseRow).count() == 0
        assert db.query(McpToolRow).count() == 0
    assert not dataset_dir.exists()
