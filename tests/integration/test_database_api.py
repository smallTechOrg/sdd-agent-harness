def _csv(*rows) -> bytes:
    buf = io.StringIO()
    w = csv.writer(buf)
    for r in rows:
        w.writerow(r)
    return buf.getvalue().encode()


def _orders() -> bytes:
    return _csv(["id", "amount"], [1, 5], [2, 7])


def _create(client, name: str, data: bytes | None = None, filename: str = "orders.csv"):
    up = client.post("/database/upload", data={"database_name": name},
                     files={"file": (filename, data or _orders(), "text/csv")})
    assert up.status_code == 200, up.text
    uri = up.json()["uri"]
    return client.post("/database", data={"name": name, "database_type": "parquet", "database_uri": uri},
                       follow_redirects=True)


def _table_count(db, database_id: str) -> int:
    """Active entity (non-schema) resources = the database's tables (no stored physical catalog)."""
    return (
        db.query(McpResourceRow)
        .filter(McpResourceRow.database_id == database_id, McpResourceRow.deleted_at.is_(None),
                McpResourceRow.kind != "schema")
        .count()
    )


def _only_server():
    with session_module.create_db_session() as db:
        srv = db.query(DatabaseRow).one()
        return srv.id, srv.version, _table_count(db, srv.id)


def test_create_builds_server_and_capabilities(client):
    r = _create(client, "Sales DB")
    assert r.status_code == 200
    with session_module.create_db_session() as db:
        srv = db.query(DatabaseRow).one()
        assert srv.name == "Sales DB" and srv.type == "parquet"
        assert srv.uri == "parquet:///Sales%20DB"
        assert srv.title and srv.description           # generated by the sync stub
        assert srv.version >= 2                         # create(1) + sync apply bumps to 2
        assert _table_count(db, srv.id) == 1            # one entity resource per inspected table
        tools = db.query(McpToolRow).filter_by(database_id=srv.id, deleted_at=None).all()
        assert len(tools) >= 1


